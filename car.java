public class car
  
{
  private String name;
  private int mpg;
  
    public void setName(String newname)
  {
    name = newname;
  }
  public void setMPG(int newmpg)
  {
    mpg = newmpg;
  }
  
  public String getName()
  {
    return name;
  }
  public int getMPG()
  {
    return mpg;
  }
}